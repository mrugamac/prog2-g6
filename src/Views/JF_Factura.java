    //metodo que limpia la tabla
    public void LimpiarTabla()
    {
        
        for(int i = 0; i < Tbl_factura.getRowCount(); i++)
        {
            DefaultTableModel m = (DefaultTableModel) Tbl_factura.getModel();
            m.removeRow(i);
            i -= 1;
        }  
    }
    
    //llenamos la tabla
    public void LlenarTabla()
    {
        Object[][] Temfa = null;
        //llamo al metodo de la clase que realiza el llenado 
        C_Factura facturas = new C_Factura();
        Temfa = facturas.cargarTabla();
        
        DefaultTableModel modelo3 = (DefaultTableModel) Tbl_factura.getModel();
        
        for(int i = 0; i < Temfa.length; i++)
        {
            modelo3.addRow(Temfa[i]);
        }
        Tbl_factura.setModel(modelo3);   
    }
    
    //mostrar data 
    public void MostrarTabla()
    { 
        int num_factura = 0;
        
        DefaultTableModel m2 = (DefaultTableModel) Tbl_factura.getModel();
        String id = String.valueOf(m2.getValueAt(Tbl_factura.getSelectedRow(), 1));
        String num_pago = String.valueOf(m2.getValueAt(Tbl_factura.getSelectedRow(), 2));
        String fecha = String.valueOf(m2.getValueAt(Tbl_factura.getSelectedRow(), 3));
        String nombre = String.valueOf(m2.getValueAt(Tbl_factura.getSelectedRow(), 4));

  
        num_factura = Integer.parseInt(id);
        String idF = Integer.toString(num_factura);
        
        Txt_idfactura.setText(idF);
        Txt_fecha.setText(fecha);
        Txt_numpago.setText(num_pago); 
        Txt_idcliente.setText(nombre);  
    }
    
    public void LimpiarCampos()
    {
        Txt_idfactura.setText("");
        Txt_fecha.setText("");
        Txt_numpago.setText(""); 
        Txt_idcliente.setText(""); 
    }
    
