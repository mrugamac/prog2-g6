    private void Btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_GuardarActionPerformed
        // TODO add your handling code here:
        int num_factura, factura;
        
        String fecha, cliente, pago;
        
        fecha = Txt_fecha.getText().toString();
        pago = Txt_numpago.getText().toString();
        factura = Integer.parseInt(Txt_idfactura.getText().toString());
        int p = Integer.parseInt(pago);
        cliente = Txt_idcliente.getText().toString();
        
        C_Factura facturas = new C_Factura();
        
        try
        {
            if(Txt_fecha.getText().toString().isEmpty() || Txt_numpago.getText().toString().isEmpty() 
                    || Txt_idfactura.getText().toString().isEmpty() || Txt_idcliente.getText().toString().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Falta ingresar informacion", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else 
            {
                num_factura = facturas.Guardar(factura, p, fecha, cliente);
            }
        }
        catch(SQLException ex)
        {
            Logger.getLogger(JF_Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        LimpiarTabla();
        LlenarTabla();
        LimpiarCampos();
    }//GEN-LAST:event_Btn_GuardarActionPerformed

