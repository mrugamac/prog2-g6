    public void LimpiarTabla()
    {
        
        for(int i = 0; i < Tbl_ModoPago.getRowCount(); i++)
        {
            DefaultTableModel m = (DefaultTableModel) Tbl_ModoPago.getModel();
            m.removeRow(i);
            i -= 1;
        }  
    }
    
    public void LlenarTabla()
    {
        
        Object[][] TemCli = null;
        //llamo al metodo de la clase que realiza el llenado 
        C_ModoPago pago = new C_ModoPago();
        TemCli = pago.cargarTabla();
        
        DefaultTableModel modelo3 = (DefaultTableModel) Tbl_ModoPago.getModel();
        
        for(int i = 0; i < TemCli.length; i++)
        {
            modelo3.addRow(TemCli[i]);
        }
        Tbl_ModoPago.setModel(modelo3);   
    }
    
    public void MostrarTabla()
    { 
        int num_pago = 0;
        
        DefaultTableModel m2 = (DefaultTableModel) Tbl_ModoPago.getModel();
        String id = String.valueOf(m2.getValueAt(Tbl_ModoPago.getSelectedRow(), 1));
        String nombre = String.valueOf(m2.getValueAt(Tbl_ModoPago.getSelectedRow(), 2));
        String detalles = String.valueOf(m2.getValueAt(Tbl_ModoPago.getSelectedRow(), 3));
        
        num_pago = Integer.parseInt(id);
        String idC = Integer.toString(num_pago);
        
        Txt_numpago.setText(id);
        Txt_nompago.setText(nombre);
        Txt_detalles.setText(detalles);
    }    
    
    public void LimpiarCampos()
    {
        Txt_numpago.setText("");
        Txt_nompago.setText("");
        Txt_detalles.setText(""); 
    }
    
    

