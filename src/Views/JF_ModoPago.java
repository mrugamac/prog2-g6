    private void Btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_agregarActionPerformed
        // TODO add your handling code here:
        int num_pago;
        String nombre, detalles;
        
        nombre = Txt_nompago.getText().toString();
        detalles = Txt_detalles.getText().toString();
        
        C_ModoPago nuevo = new C_ModoPago();
        
        try
        {
            if(Txt_nompago.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Falta ingresar el nombre del cliente", "Error", JOptionPane.ERROR_MESSAGE);
            }
            else if(Txt_detalles.getText().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Falta ingresar detalles", "Error", JOptionPane.ERROR_MESSAGE);
            }
            num_pago = nuevo.Guardar(nombre, detalles);
        }
        catch(SQLException ex)
        {
            Logger.getLogger(JF_Cliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        LimpiarTabla();
        LlenarTabla();
        LimpiarCampos();
    }//GEN-LAST:event_Btn_agregarActionPerformed

