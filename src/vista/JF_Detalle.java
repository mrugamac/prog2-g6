    private void Btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_eliminarActionPerformed
        // TODO add your handling code here:
        boolean elimina = false;
        int num_detalle =  Integer.parseInt(Txt_numdetalle.getText());
        
        C_Detallle de = new C_Detallle();

        try
        {
            elimina = de.Eliminar(num_detalle);
            if(elimina == true)
            {
                JOptionPane.showMessageDialog(null, "Se eliminó la información ", "Información", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "No se eliminó la información ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException ex)
        {

        }
        LimpiarTabla();
        LlenarTabla();
        LimpiarCampos();
    }//GEN-LAST:event_Btn_eliminarActionPerformed

