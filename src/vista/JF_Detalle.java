    //metodo que limpia la tabla
    public void LimpiarTabla()
    {
        
        for(int i = 0; i < Tbl_Detalle.getRowCount(); i++)
        {
            DefaultTableModel m = (DefaultTableModel) Tbl_Detalle.getModel();
            m.removeRow(i);
            i -= 1;
        }  
    }
    
    //llenamos la tabla
    public void LlenarTabla()
    {
        
        Object[][] TemDet = null;
        //llamo al metodo de la clase que realiza el llenado 
        C_Detallle det = new C_Detallle();
        TemDet = det.cargarTabla();
        
        DefaultTableModel modelo3 = (DefaultTableModel) Tbl_Detalle.getModel();
        
        for(int i = 0; i < TemDet.length; i++)
        {
            modelo3.addRow(TemDet[i]);
        }
        Tbl_Detalle.setModel(modelo3);   
    }
    
    //mostrar data 
    public void MostrarTabla()
    { 
        int num_detalle = 0, can = 0;
        
        DefaultTableModel m2 = (DefaultTableModel) Tbl_Detalle.getModel();
        String id = String.valueOf(m2.getValueAt(Tbl_Detalle.getSelectedRow(), 1));
        String factura = String.valueOf(m2.getValueAt(Tbl_Detalle.getSelectedRow(), 2));
        String cantidad = String.valueOf(m2.getValueAt(Tbl_Detalle.getSelectedRow(), 3));
        String precio = String.valueOf(m2.getValueAt(Tbl_Detalle.getSelectedRow(), 4));
       
        can = Integer.parseInt(cantidad);
        
        num_detalle = Integer.parseInt(id);
        String idC = Integer.toString(num_detalle);
        
        Txt_numdetalle.setText(idC);
        Txt_numfactura.setText(factura);
        Txt_precio.setText(precio); 
        
        if(Cmb_Cantidad.getSelectedIndex() == 0)
        {
            can = 1;
        }
        else if(Cmb_Cantidad.getSelectedIndex() == 1)
        {
            can = 1;
        }
    }
    
    public void LimpiarCampos()
    {
        Txt_numdetalle.setText("");
        Txt_numfactura.setText("");
        Txt_precio.setText(""); 
        Cmb_Cantidad.setSelectedIndex(0);
    }

